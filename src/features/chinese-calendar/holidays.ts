const holidaySet = new Map<string, "holiday" | "workday">()

for (const year of Object.values(holidayData)) {
  for (const d of year.holidays) {
    holidaySet.set(d, "holiday")
  }
  for (const d of year.workdays) {
    holidaySet.set(d, "workday")
  }
}

export function getHolidayType(
  isoDate: string
): "holiday" | "workday" | null {
  return holidaySet.get(isoDate) ?? null
}
